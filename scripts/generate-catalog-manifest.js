const fs = require("fs");
const path = require("path");

const compiledIndexPath = path.resolve(__dirname, "..", ".tmp", "validation", "src", "data", "index.js");
const outputPath = path.resolve(__dirname, "..", "src", "data", "catalog-manifest.ts");

if (!fs.existsSync(compiledIndexPath)) {
  throw new Error("Compiled catalog not found. Run `npm run prepare:data` first.");
}

const { DISEASE_STATES } = require(compiledIndexPath);

const diseaseCatalog = DISEASE_STATES.map((disease) => ({
  id: disease.id,
  name: disease.name,
  icon: disease.icon,
  category: disease.category,
  subcategoryCount: disease.subcategories.length,
  monographCount: disease.drugMonographs.length,
  contentMeta: disease.contentMeta ?? undefined,
}));

const seen = new Set();
const monographCatalog = [];
for (const disease of DISEASE_STATES) {
  for (const monograph of disease.drugMonographs) {
    if (seen.has(monograph.id)) continue;
    seen.add(monograph.id);
    monographCatalog.push({
      id: monograph.id,
      name: monograph.name,
      drugClass: monograph.drugClass,
      parentDiseaseId: disease.id,
      parentDiseaseName: disease.name,
    });
  }
}

const file = `import type { DiseaseCatalogSummary, MonographCatalogSummary } from "../types";

// AUTO-GENERATED by scripts/generate-catalog-manifest.js
// Run \`npm run generate:catalog-manifest\` after changing disease content.

export const DISEASE_CATALOG: DiseaseCatalogSummary[] = ${JSON.stringify(diseaseCatalog, null, 2)};

export const MONOGRAPH_CATALOG: MonographCatalogSummary[] = ${JSON.stringify(monographCatalog, null, 2)};

export const DISEASE_SUMMARY_BY_ID: Record<string, DiseaseCatalogSummary> = Object.fromEntries(
  DISEASE_CATALOG.map((disease) => [disease.id, disease]),
);

export const MONOGRAPH_SUMMARY_BY_ID: Record<string, MonographCatalogSummary> = Object.fromEntries(
  MONOGRAPH_CATALOG.map((monograph) => [monograph.id, monograph]),
);
`;

fs.writeFileSync(outputPath, file);
console.log(`Wrote ${path.relative(process.cwd(), outputPath)}`);
