const fs = require("fs");
const path = require("path");

const compiledEditorialPath = path.resolve(
  __dirname,
  "..",
  ".tmp",
  "validation",
  "src",
  "data",
  "editorial",
  "content-meta.js",
);
const outputPath = path.resolve(__dirname, "..", "src", "data", "generated-content-meta.ts");

if (!fs.existsSync(compiledEditorialPath)) {
  throw new Error("Compiled editorial metadata not found. Run `npm run prepare:data` first.");
}

const {
  DISEASE_CONTENT_META,
  SUBCATEGORY_CONTENT_META,
  MONOGRAPH_CONTENT_META,
  PRIORITY_SUBCATEGORY_META_KEYS,
  PRIORITY_MONOGRAPH_META_KEYS,
} = require(compiledEditorialPath);

const file = `import type { ContentMetaSeed } from "./editorial/content-meta";

// AUTO-GENERATED by scripts/generate-content-meta.js
// Run \`npm run generate:content-meta\` after changing editorial metadata.

export const DISEASE_CONTENT_META: Record<string, ContentMetaSeed> = ${JSON.stringify(DISEASE_CONTENT_META, null, 2)};

export const SUBCATEGORY_CONTENT_META: Record<string, ContentMetaSeed> = ${JSON.stringify(SUBCATEGORY_CONTENT_META, null, 2)};

export const MONOGRAPH_CONTENT_META: Record<string, ContentMetaSeed> = ${JSON.stringify(MONOGRAPH_CONTENT_META, null, 2)};

export const PRIORITY_SUBCATEGORY_META_KEYS = ${JSON.stringify(PRIORITY_SUBCATEGORY_META_KEYS, null, 2)} as const;

export const PRIORITY_MONOGRAPH_META_KEYS = ${JSON.stringify(PRIORITY_MONOGRAPH_META_KEYS, null, 2)} as const;
`;

fs.writeFileSync(outputPath, file);
console.log(`Wrote ${path.relative(process.cwd(), outputPath)}`);
