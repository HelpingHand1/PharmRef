const fs = require("fs");
const path = require("path");
const { GENERATED_DISEASE_MODULES } = require("./disease-module-config");

const compiledDataDir = path.resolve(__dirname, "..", ".tmp", "disease-generation", "src", "data");
const generatedRootDir = path.resolve(__dirname, "..", "src", "data", "generated");
const generatedDiseaseDir = path.join(generatedRootDir, "diseases");

fs.mkdirSync(generatedDiseaseDir, { recursive: true });

const normalizeModulePath = path.join(compiledDataDir, "normalize-empiric-options.js");
const regimenCatalogModulePath = path.join(compiledDataDir, "regimen-catalog.js");

if (!fs.existsSync(normalizeModulePath)) {
  throw new Error(`Compiled empiric normalizer not found: ${normalizeModulePath}`);
}

if (!fs.existsSync(regimenCatalogModulePath)) {
  throw new Error(`Compiled regimen catalog helper not found: ${regimenCatalogModulePath}`);
}

const { normalizeDiseaseEmpiricOptions } = require(normalizeModulePath);
const { buildRegimenCatalog } = require(regimenCatalogModulePath);

const authoredDiseases = GENERATED_DISEASE_MODULES.map((moduleConfig) => {
  const compiledModulePath = path.join(compiledDataDir, moduleConfig.sourceFile);
  if (!fs.existsSync(compiledModulePath)) {
    throw new Error(`Compiled disease module not found: ${compiledModulePath}`);
  }

  const compiledModule = require(compiledModulePath);
  const disease = compiledModule[moduleConfig.exportName];
  if (!disease) {
    throw new Error(`Export "${moduleConfig.exportName}" not found in ${compiledModulePath}`);
  }

  return {
    moduleConfig,
    disease,
  };
});

const knownMonographIds = new Set(
  authoredDiseases.flatMap(({ disease }) => disease.drugMonographs.map((monograph) => monograph.id)),
);

const normalizedDiseases = authoredDiseases.map(({ moduleConfig, disease }) => ({
  moduleConfig,
  disease: normalizeDiseaseEmpiricOptions(disease, knownMonographIds),
}));

for (const { moduleConfig, disease } of normalizedDiseases) {
  const outputPath = path.join(generatedDiseaseDir, moduleConfig.outputFile);
  const file = `import type { DiseaseState } from "../../../types";

// AUTO-GENERATED by scripts/generate-disease-modules.js
// Run \`npm run generate:disease-modules\` after changing the editorial source module.

export const ${moduleConfig.exportName}: DiseaseState = ${JSON.stringify(disease, null, 2)};
`;

  fs.writeFileSync(outputPath, file);
  console.log(`Wrote ${path.relative(process.cwd(), outputPath)}`);
}

const { regimens, xrefByMonographId } = buildRegimenCatalog(normalizedDiseases.map(({ disease }) => disease));
const regimenCatalogPath = path.join(generatedRootDir, "regimen-catalog.ts");
const regimenCatalogFile = `import type { RegimenReference } from "../../types";

// AUTO-GENERATED by scripts/generate-disease-modules.js
// Run \`npm run generate:disease-modules\` after changing the editorial source module.

export const REGIMEN_CATALOG: RegimenReference[] = ${JSON.stringify(regimens, null, 2)};

export const REGIMEN_BY_ID: Record<string, RegimenReference> = Object.fromEntries(
  REGIMEN_CATALOG.map((regimen) => [regimen.id, regimen]),
);

export const REGIMEN_XREF_BY_MONOGRAPH_ID: Record<string, RegimenReference[]> = ${JSON.stringify(xrefByMonographId, null, 2)};
`;

fs.writeFileSync(regimenCatalogPath, regimenCatalogFile);
console.log(`Wrote ${path.relative(process.cwd(), regimenCatalogPath)}`);
